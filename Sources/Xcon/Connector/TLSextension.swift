import Foundation
import Security
extension SSLSessionState:CustomStringConvertible{
    public var description: String {
        switch self {
        case .idle:
            return "Idle"
        case .handshake:
            return "handshake"
        case .closed:
            return "closed"
        case .aborted:
            return "aborted"
            
        case .connected:
            return "connected"
        @unknown default:
            return "unknown"
        }
    }
}
extension SSLClientCertificateState:CustomStringConvertible {
    public var description: String {
        switch self {
        case .certNone:
            return "certNone"
        case .certRejected:
            return "certRejected"
        case .certRequested:
            return "certRequested"
        case .certSent:
            return "certSent"
        @unknown default:
             return "unknown"
        }
    }
    
    
}
